package com.its.me.model;

public class AttendanceObj {
	private String num;
	private String name;
	private String depname;
	private String atdate;
	private String atpre;
	private String ckin;
	private String ckout;
	private String cname;
	private String ccode;

	public String getNum() {
		return num;
	}

	public void setNum(String num) {
		this.num = num;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDepname() {
		return depname;
	}

	public void setDepname(String depname) {
		this.depname = depname;
	}

	public String getAtdate() {
		return atdate;
	}

	public void setAtdate(String atdate) {
		this.atdate = atdate;
	}

	public String getAtpre() {
		return atpre;
	}

	public void setAtpre(String atpre) {
		this.atpre = atpre;
	}

	public String getCkin() {
		return ckin;
	}

	public void setCkin(String ckin) {
		this.ckin = ckin;
	}

	public String getCkout() {
		return ckout;
	}

	public void setCkout(String ckout) {
		this.ckout = ckout;
	}

	public String getCname() {
		return cname;
	}

	public void setCname(String cname) {
		this.cname = cname;
	}

	public String getCcode() {
		return ccode;
	}

	public void setCcode(String ccode) {
		this.ccode = ccode;
	}

}

package com.its.me.model;


public class MemberObj{

	private String num;
	private String name;
	private String email;
	private String birth;
	private String phone;
	private int auth;
	private String belcode;
	private String belname;
	private String depcode;
	private String depname;
	private String address;
	
	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getBirth() {
		return birth;
	}
	public void setBirth(String birth) {
		this.birth = birth;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public int getAuth() {
		return auth;
	}
	public void setAuth(int auth) {
		this.auth = auth;
	}
	public String getBelcode() {
		return belcode;
	}
	public void setBelcode(String belcode) {
		this.belcode = belcode;
	}
	public String getBelname() {
		return belname;
	}
	public void setBelname(String belname) {
		this.belname = belname;
	}
	public String getDepcode() {
		return depcode;
	}
	public void setDepcode(String depcode) {
		this.depcode = depcode;
	}
	public String getDepname() {
		return depname;
	}
	public void setDepname(String depname) {
		this.depname = depname;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	

}
